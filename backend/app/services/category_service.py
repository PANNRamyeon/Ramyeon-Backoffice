    def _is_valid_category_id(self, category_id):
        """Check if category ID format is valid (CAT-####)."""
        return validate_category_id(category_id)

    def _ensure_subcategory_present(self, category_id, category_doc, subcategory_name):
        """
        Legacy method – ensures a subcategory exists.
        category_doc is kept for signature compatibility but not used.
        """
        category = Category.get_by_id(category_id)
        if not category:
            return
        if not any(sc.name == subcategory_name for sc in category.sub_categories):
            category.add_subcategory(
                name=subcategory_name,
                description=f'Default {subcategory_name} subcategory'
            )

