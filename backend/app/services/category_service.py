    def move_product_to_none_subcategory(self, product_id, category_id, current_user=None):
        """Move product to the default 'None' subcategory within the same category."""
        try:
            logger.info(f"Moving product {product_id} to '{self.DEFAULT_SUBCATEGORY_NAME}' subcategory in category {category_id}")

            if not validate_product_id(product_id) or not self._is_valid_category_id(category_id):
                raise ValueError("Invalid product or category ID format")

            product = Product.get_by_id(product_id)
            if not product:
                raise ValueError(f"Product {product_id} not found")

            # Ensure 'None' subcategory exists
            category = Category.get_by_id(category_id)
            if not category:
                raise ValueError(f"Category {category_id} not found")
            self._ensure_subcategory_present(category_id, None, self.DEFAULT_SUBCATEGORY_NAME)

            # Update product
            success = self._update_product_category(
                product_id=product_id,
                category_id=category_id,
                subcategory_name=self.DEFAULT_SUBCATEGORY_NAME,
                current_user=current_user
            )
            if not success:
                raise Exception("Failed to move product")

            return {
                'success': True,
                'action': 'moved_to_default',
                'message': f"Product '{product.product_name}' moved to '{self.DEFAULT_SUBCATEGORY_NAME}' subcategory"
            }
        except Exception as e:
            logger.error(f"Error moving product to None subcategory: {e}")
            raise Exception(f"Error moving product to None subcategory: {str(e)}")

    def move_product_to_category(self, product_id, new_category_id, new_subcategory_name=None, current_user=None):
        """Move a product to a different category/subcategory."""
        try:
            logger.info(f"Moving product {product_id} to category {new_category_id}")

            product = Product.get_by_id(product_id)
            if not product:
                raise ValueError(f"Product {product_id} not found")

            target_category = Category.get_by_id(new_category_id)
            if not target_category:
                raise ValueError(f"Target category {new_category_id} not found")

            new_sub = new_subcategory_name or self.DEFAULT_SUBCATEGORY_NAME

            # Ensure subcategory exists
            if not any(sc.name == new_sub for sc in target_category.sub_categories):
                if new_sub == self.DEFAULT_SUBCATEGORY_NAME:
                    self._ensure_subcategory_present(new_category_id, None, new_sub)
                else:
                    raise ValueError(f"Subcategory '{new_sub}' not found in category {new_category_id}")

            old_cat = product.category_id
            old_sub = product.subcategory_name

            success = self._update_product_category(
                product_id=product_id,
                category_id=new_category_id,
                subcategory_name=new_sub,
                current_user=current_user
            )
            if not success:
                raise Exception("Failed to move product")

            self._send_category_notification(
                'product_moved',
                target_category.category_name,
                new_category_id,
                {
                    'product_id': product_id,
                    'product_name': product.product_name,
                    'from_category': old_cat,
                    'from_subcategory': old_sub,
                    'to_subcategory': new_sub
                }
            )

            return {
                'success': True,
                'product_id': product_id,
                'product_name': product.product_name,
                'old_category': old_cat,
                'old_subcategory': old_sub,
                'new_category': new_category_id,
                'new_subcategory': new_sub
            }
        except Exception as e:
            logger.error(f"Error moving product to category: {e}")
            raise Exception(f"Error moving product to category: {str(e)}")

