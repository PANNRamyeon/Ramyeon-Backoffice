    def add_product_to_subcategory(self, category_id, subcategory_name, product_identifier, current_user=None):
        """Add product to subcategory."""
        try:
            logger.info(f"Adding product '{product_identifier}' to subcategory '{subcategory_name}' in category {category_id}")

            if not self._is_valid_category_id(category_id):
                raise ValueError("Invalid category ID")

            product = self._get_product_by_identifier(product_identifier)
            if not product:
                raise ValueError(f"Product '{product_identifier}' not found")

            category = Category.get_by_id(category_id)
            if not category or category.isDeleted:
                raise ValueError("Category not found or deleted")

            # Check if subcategory exists
            if not any(sc.name == subcategory_name for sc in category.sub_categories):
                if subcategory_name == self.DEFAULT_SUBCATEGORY_NAME:
                    self._ensure_subcategory_present(category_id, None, subcategory_name)
                    category = Category.get_by_id(category_id)  # refresh
                else:
                    raise ValueError(f"Subcategory '{subcategory_name}' not found in category")

            # Update product category
            success = self._update_product_category(
                product_id=product.sk,
                category_id=category_id,
                subcategory_name=subcategory_name,
                current_user=current_user
            )
            if not success:
                raise Exception("Failed to update product category")

            # Send notification
            self._send_category_notification(
                'product_added',
                category.category_name,
                category_id,
                {
                    'subcategory_name': subcategory_name,
                    'product_id': product.sk,
                    'product_name': product.product_name,
                    'added_by': current_user.get('username') if current_user else 'system'
                }
            )

            # Audit logging
            if current_user and self.audit_service:
                self._log_product_category_audit(
                    action='add_product_to_subcategory',
                    user=current_user,
                    category=category,
                    product=product,
                    details={'subcategory_name': subcategory_name, 'action': 'added'}
                )

            return {
                'success': True,
                'action': 'added',
                'product_id': product.sk,
                'product_name': product.product_name,
                'category_id': category_id,
                'subcategory_name': subcategory_name,
                'message': f"Product '{product.product_name}' added to subcategory '{subcategory_name}'"
            }
        except Exception as e:
            logger.error(f"Error adding product to subcategory: {e}")
            raise Exception(f"Error adding product to subcategory: {str(e)}")

    def _remove_product_from_all_subcategories(self, product_id):
        """
        Remove product from all subcategories.
        In DynamoDB, we simply update the product to have no category.
        """
        product = Product.get_by_id(product_id)
