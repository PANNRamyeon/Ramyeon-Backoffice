    def _ensure_tables_exist(self):
        """Create DynamoDB tables if not exist."""
        try:
            if not Category.exists():
                Category.create_table(wait=True)
                logger.info("Category table created")
            if not Product.exists():
                Product.create_table(wait=True)
                logger.info("Product table created")
        except Exception as e:
            logger.error(f"Error ensuring tables: {e}")

