    def remove_subcategory(self, category_id, subcategory_name, current_user=None):
        """Remove a subcategory from a category – moves its products to 'General'."""
        try:
            logger.info(f"Removing subcategory '{subcategory_name}' from category {category_id}")
            if not self._is_valid_category_id(category_id):
                return False

            category = Category.get_by_id(category_id)
            if not category or category.isDeleted:
                raise ValueError("Category not found or is deleted")

            # Find subcategory ID by name
            subcategory = None
            for sc in category.sub_categories:
                if sc.name == subcategory_name:
                    subcategory = sc
                    break

            if not subcategory:
                raise ValueError(f"Subcategory '{subcategory_name}' not found")

            # Check if subcategory has products
            product_count = self._get_product_count_by_subcategory(category.sk, subcategory_name)
            if product_count > 0:
                # Move products to 'General' subcategory
                moved = self.product_collection.update_many(
                    {'category_id': category_id, 'subcategory_name': subcategory_name},
                    {'$set': {'subcategory_name': 'General', 'updated_at': datetime.utcnow()}}
                ).modified_count
                logger.info(f"Moved {moved} products to 'General' subcategory")
            else:
                moved = 0

            success = category.remove_subcategory(subcategory.subcategory_id)
            if success:
                self._send_category_notification(
                    'subcategory_removed',
                    category.category_name,
                    category_id,
                    {
                        'subcategory_name': subcategory_name,
                        'subcategory_id': subcategory.subcategory_id,
                        'removed_by': current_user.get('username') if current_user else 'system',
                        'products_moved': moved
                    }
                )

                # Audit logging
                if current_user and self.audit_service:
                    self._log_category_audit(
                        action='remove_subcategory',
                        user=current_user,
                        category=category,
                        details={
                            'subcategory_name': subcategory_name,
                            'subcategory_id': subcategory.subcategory_id,
                            'products_moved': moved
                        }
                    )

                logger.info(f"Subcategory '{subcategory_name}' removed successfully, {moved} products moved")
                return True
            return False
        except Exception as e:
            logger.error(f"Error removing subcategory: {e}", exc_info=True)
            raise Exception(f"Error removing subcategory: {str(e)}")

    def get_subcategories(self, category_id):
        """Get all subcategories for a category with product counts."""
        try:
            if not self._is_valid_category_id(category_id):
                return []
            category = Category.get_by_id(category_id)
            if not category or category.isDeleted:
                return []
            result = []
            for sc in category.sub_categories:
                sc_dict = sc.to_dict()
                sc_dict['product_count'] = self._get_product_count_by_subcategory(category_id, sc.name)
                result.append(sc_dict)
            return result
        except Exception as e:
            logger.error(f"Error getting subcategories: {e}")
            raise Exception(f"Error getting subcategories: {str(e)}")

