    def _ensure_tables_exist(self):
        """Ensure DynamoDB tables exist (called at startup)."""
        try:
            if not Category.exists():
                Category.create_table(wait=True)
                logger.info("Category table created")
            if not Product.exists():
                Product.create_table(wait=True)
                logger.info("Product table created")
        except Exception as e:
            logger.error(f"Error ensuring tables: {e}")

    # -----------------------------------------------------------------
    # UTILITY METHODS (preserved signatures)
    # -----------------------------------------------------------------
    def _is_valid_category_id(self, category_id):
        """Check if category ID format is valid (CAT-####)."""
        return validate_category_id(category_id)

    def _ensure_subcategory_present(self, category_id, category_doc, subcategory_name):
        """
        Legacy method – ensures a subcategory exists.
        category_doc is kept for signature compatibility but not used.
        """
        category = Category.get_by_id(category_id)
        if not category:
            return
        if not any(sc.name == subcategory_name for sc in category.sub_categories):
            category.add_subcategory(
                name=subcategory_name,
                description=f'Default {subcategory_name} subcategory'
            )

    def generate_category_id(self):
        """
        Generate next CAT-#### ID.
        In DynamoDB, IDs are generated by the model; this method is kept for compatibility.
        """
        return generate_sk('CAT-', 'category_seq')

    def generate_subcategory_id(self):
        """Generate next SUB-##### ID."""
        return generate_sk('SUB-', 'subcategory_seq')

    def _send_category_notification(self, action_type, category_name, category_id=None, additional_metadata=None):
        """Send notification for category actions."""
        templates = {
            'created':          {'title': 'Category Created',          'priority': 'medium'},
            'updated':          {'title': 'Category Updated',         'priority': 'medium'},
            'soft_deleted':     {'title': 'Category Deleted',         'priority': 'medium'},
            'hard_deleted':     {'title': 'Category Permanently Deleted', 'priority': 'high'},
            'restored':         {'title': 'Category Restored',        'priority': 'medium'},
            'bulk_deleted':     {'title': 'Categories Bulk Deleted',  'priority': 'low'},
            'bulk_updated':     {'title': 'Categories Bulk Updated',  'priority': 'low'},
            'subcategory_added':   {'title': 'Subcategory Added',     'priority': 'low'},
            'subcategory_removed': {'title': 'Subcategory Removed',   'priority': 'low'},
            'products_moved':   {'title': 'Products Moved',           'priority': 'low'},
        }
        tpl = templates.get(action_type)
        if not tpl:
            return
        metadata = {
            "category_id": str(category_id) if category_id else "",
            "category_name": category_name,
            "action_type": f"category_{action_type}",
            **(additional_metadata or {})
        }
        notification_service.create_notification(
            title=tpl['title'],
            message=f"{tpl['title']}: {category_name}" if category_name else tpl['title'],
            priority=tpl['priority'],
            notification_type="system",
            metadata=metadata,
            action_type=f"category_{action_type}"
        )

    # -----------------------------------------------------------------
