    def get_category_stats(self):
        """Get comprehensive category statistics."""
        try:
            from ...models.Categories import CategoryManager
            stats = CategoryManager.get_category_statistics()

            total_products = 0
            for cat in Category.get_all_categories(include_deleted=False):
                total_products += self._get_product_count_by_category(cat.sk)

            stats['total_products'] = total_products
            stats['avg_products_per_category'] = total_products / stats['total_categories'] if stats['total_categories'] else 0
            return stats
        except Exception as e:
            logger.error(f"Error getting category stats: {e}")
            return {
                'total_categories': 0,
                'active_categories': 0,
                'deleted_categories': 0,
                'total_subcategories': 0,
                'total_products': 0
            }

    def get_category_delete_info(self, category_id):
        """Get information about a category before deletion."""
        try:
            if not self._is_valid_category_id(category_id):
                return None
            category = Category.get_by_id(category_id)
            if not category:
                return None

            subcategories_count = len(category.sub_categories)
            products_count = self._get_product_count_by_category(category_id)

            return {
                'category_id': category.sk,
                'category_name': category.category_name,
                'description': category.description,
                'status': category.status,
                'isDeleted': category.isDeleted,
                'subcategories_count': subcategories_count,
                'products_count': products_count,
                'can_soft_delete': not category.isDeleted,
                'can_restore': category.isDeleted,
                'created_at': category.date_created.isoformat() if category.date_created else None,
                'last_updated': category.last_updated.isoformat() if category.last_updated else None,
                'deleted_at': category.last_updated.isoformat() if category.isDeleted else None
            }
        except Exception as e:
            logger.error(f"Error getting category delete info: {e}")
            raise Exception(f"Error getting category delete info: {str(e)}")

    def bulk_soft_delete_categories(self, category_ids, current_user=None):
        """Soft delete multiple categories at once."""
        try:
            if not category_ids:
                return {'success': 0, 'failed': 0, 'errors': []}

            logger.info(f"Bulk soft deleting {len(category_ids)} categories")
            success_count = 0
            failed_count = 0
            errors = []

            for cid in category_ids:
                try:
                    if self.soft_delete_category(cid, current_user, "bulk_delete"):
                        success_count += 1
                    else:
                        failed_count += 1
                        errors.append(f"Failed to soft delete {cid}")
                except Exception as e:
                    failed_count += 1
                    errors.append(f"Error deleting {cid}: {str(e)}")

            if success_count > 0:
                self._send_category_notification(
                    'bulk_deleted',
                    f"{success_count} categories",
                    None,
                    {
                        'success_count': success_count,
                        'failed_count': failed_count,
                        'action_type': 'categories_bulk_delete'
                    }
                )

            return {
                'success': success_count,
                'failed': failed_count,
                'errors': errors,
                'total_requested': len(category_ids)
            }
        except Exception as e:
            logger.error(f"Error in bulk soft delete: {e}")
            raise Exception(f"Error in bulk soft delete: {str(e)}")

    def bulk_update_categories_status(self, category_ids, new_status, current_user=None):
        """Efficiently update multiple categories status."""
        try:
            if new_status not in ['active', 'inactive']:
                raise ValueError("Status must be 'active' or 'inactive'")

            logger.info(f"Bulk updating {len(category_ids)} categories to status: {new_status}")

            success_count = 0
            failed_count = 0
            errors = []

            for cid in category_ids:
                try:
                    cat = Category.get_by_id(cid)
                    if cat and not cat.isDeleted:
                        cat.update_category(status=new_status)
                        success_count += 1
                    else:
                        failed_count += 1
                        errors.append(f"Category {cid} not found or deleted")
                except Exception as e:
                    failed_count += 1
                    errors.append(f"Error updating {cid}: {str(e)}")

            if success_count > 0:
                self._send_category_notification(
                    'bulk_updated',
                    f"{success_count} categories",
                    None,
                    {
                        'success_count': success_count,
                        'new_status': new_status,
                        'action_type': 'categories_bulk_status_update'
                    }
                )

            return {
                'success': success_count,
                'total_requested': len(category_ids)
            }
        except Exception as e:
            logger.error(f"Bulk update failed: {e}")
            raise Exception(f"Bulk update failed: {str(e)}")

