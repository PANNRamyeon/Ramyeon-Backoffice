    def _enrich_category(self, category):
        """Add product counts to category dict."""
        cat_dict = category.to_dict()
        cid = category.sk
        cat_dict['product_count'] = self._get_product_count_by_category(cid)
        for sub in cat_dict.get('sub_categories', []):
            sub['product_count'] = self._get_product_count_by_subcategory(cid, sub['name'])
        return cat_dict

    def _get_product_count_by_category(self, category_id):
        """Count products in a category using product_service."""
        return self.product_service._get_product_count_by_category(category_id)

    def _get_product_count_by_subcategory(self, category_id, subcategory_name):
        """Count products in a subcategory using product_service."""
        return self.product_service._get_product_count_by_subcategory(category_id, subcategory_name)

    def _log_category_audit(self, action, user, category, old_values=None, new_values=None, details=None):
        """Log category audit event."""
        try:
            import json
            changes = None
            if old_values or new_values:
                changes = json.dumps({
                    "old_values": self._convert_to_serializable(old_values) or {},
                    "new_values": self._convert_to_serializable(new_values) or {},
                    "changed_fields": list(new_values.keys()) if new_values else []
                })
            self.audit_service._create_audit_log(
                event_type=f"category_{action}",
                user_data=user,
                target_data={
                    "type": "category",
                    "id": category.sk,
                    "name": category.category_name
                },
                old_values=old_values,
                new_values=new_values,
                metadata=details,
                changes=changes
            )
        except Exception as e:
            logger.error(f"Failed to log category audit: {e}")

    def _convert_to_serializable(self, obj):
        """Convert object to JSON serializable format."""
        if isinstance(obj, dict):
            return {k: self._convert_to_serializable(v) for k, v in obj.items()}
        if isinstance(obj, list):
            return [self._convert_to_serializable(i) for i in obj]
        if isinstance(obj, datetime):
            return obj.isoformat()
        return obj